package com.nbicocchi.tutorials.basics;

public class PrimitiveTypes {
    public static void main(String[] args) {
        byte a = 13;
        /* char is actually a 16 bit unsigned int */
        char b = 65;
        short c = 34;
        int d = 332;
        long e = 122;
        float f = 7.6F;
        double g = 12.3;
        boolean h = true;
    }
}
